public class Calculator {

    public Calculator(){
    }

    public int add(int input_1, int input_2){
        return input_1 + input_2;
    }

    public int subtract(int input_1, int input_2){
        return input_1 - input_2;
    }

    public int multiply(int input_1, int input_2){
        return input_1 * input_2;
    }

    public double devide(double input_1, double input_2){
        return input_1 / input_2;
    }
}

